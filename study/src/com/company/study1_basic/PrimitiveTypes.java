package com.company.study1_basic;

public class PrimitiveTypes {

    public static void main(String[] args) {
// 숫자 타입
// 정수형
        int i = 0;
        long l = 12345676987689876l;
// 소수점
        float f = 0.123f;
        double d = 0.128379123d;

// 문자형
// 글자 단 하나 표현
        char c1 = 'C'; // ''으로 써야함
        char c2 = '\u0000'; // unicode 사용 가능

// 글자 여러개 표개
        String str = "글자가 여러개 wow"; // ""으로 써야함

// 논리형(참/거짓) 형
        boolean bt = true;
        boolean bf = false;
    }
}
